package com.lottejtb.api.service;

public class HeaderVO {
	private String deviceCd;
	private String agentTy;
	private String agentId;
	
	
	public String getDeviceCd() {
		return deviceCd;
	}
	public void setDeviceCd(String deviceCd) {
		this.deviceCd = deviceCd;
	}
	public String getAgentTy() {
		return agentTy;
	}
	public void setAgentTy(String agentTy) {
		this.agentTy = agentTy;
	}
	public String getAgentId() {
		return agentId;
	}
	public void setAgentId(String agentId) {
		this.agentId = agentId;
	}	
}
