package com.lottejtb.api.service;

public class BodyVO {
	private int adtCnt;
	private String availTy;
	private String cabinClas;
	private String carrierCd;
	private int chdCnt;
	private String gdsPnr;
	private int infCnt;
	
	
	public int getAdtCnt() {
		return adtCnt;
	}
	public void setAdtCnt(int adtCnt) {
		this.adtCnt = adtCnt;
	}
	public String getAvailTy() {
		return availTy;
	}
	public void setAvailTy(String availTy) {
		this.availTy = availTy;
	}
	public String getCabinClas() {
		return cabinClas;
	}
	public void setCabinClas(String cabinClas) {
		this.cabinClas = cabinClas;
	}
	public String getCarrierCd() {
		return carrierCd;
	}
	public void setCarrierCd(String carrierCd) {
		this.carrierCd = carrierCd;
	}
	public int getChdCnt() {
		return chdCnt;
	}
	public void setChdCnt(int chdCnt) {
		this.chdCnt = chdCnt;
	}
	public String getGdsPnr() {
		return gdsPnr;
	}
	public void setGdsPnr(String gdsPnr) {
		this.gdsPnr = gdsPnr;
	}
	public int getInfCnt() {
		return infCnt;
	}
	public void setInfCnt(int infCnt) {
		this.infCnt = infCnt;
	}
}
